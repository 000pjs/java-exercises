public class Loops {

    public static void main(String[] args) {

        System.out.println("printZeroToTen :: ");
        printZeroToTen();

    }

    // Print integers from 0 to 10
    public static void printZeroToTen() {

        for (int i = 0; i < 11; i++) {
            System.out.println(i);
        }
    }
}
